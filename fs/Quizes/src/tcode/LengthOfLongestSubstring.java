    public static boolean isUniqueChar(String str) {
        if (str.length() > 128)
            return false;
        boolean[] charSet = new boolean[128];

        for (int i = 0; i < str.length(); ++i) {
            int temp = str.charAt(i);
            if (charSet[temp])
                return false;
            charSet[temp] = true;
        }
        return true;
    }


